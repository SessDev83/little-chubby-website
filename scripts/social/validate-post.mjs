/**
 * validate-post.mjs — deterministic QA gate for AI-generated social posts.
 *
 * Implements the 20 blocking rules (V1-V20) and 4 warnings (W1-W4) defined in
 * GROWTH_CONTENT_PLAYBOOK.md §11. Pure, side-effect free, no network calls.
 *
 * USAGE (as a library):
 *   import { validatePost, injectUtms } from "./validate-post.mjs";
 *   const result = validatePost(post, { type, lang, history });
 *   if (!result.ok) { ... retry or skip ... }
 *
 * USAGE (as a CLI, for ad-hoc testing):
 *   node scripts/social/validate-post.mjs --file path/to/post.json --type free-coloring --lang en
 *   node scripts/social/validate-post.mjs --stdin --type free-coloring --lang en  < post.json
 *
 * The post object shape (matches generateAIPost output in ai-generate.mjs):
 * {
 *   concept: string,
 *   creativeId?: string,
 *   flywheelStage?: number,
 *   platforms: {
 *     bluesky:   { text, hashtags },
 *     facebook:  { text, hashtags },
 *     instagram: { text, hashtags },
 *   }
 * }
 *
 * Context shape:
 * {
 *   type: "book-promo" | "free-coloring" | ... ,
 *   lang: "en" | "es",
 *   history?: Array<{ type: string, postedAt: string, platforms: {...} }>,
 *   realGalleryReviewCount?: number,  // for V19
 * }
 */

